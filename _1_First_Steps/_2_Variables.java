public class _2_Variables {
    public static void main(String[] args) {
        // see different primitive data types
        int num = 6;
        System.out.println(num);
        num = 100;
        System.out.println(num);
        num = 5 + 4;
        System.out.println(num);
        num = (4 * 4) + (5 - 2);
        System.out.println(num);
        // try /list command in jshell to lookup all your history

    }
}
